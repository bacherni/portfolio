fixed=PowerGridFixedPoint[FilterRules[{opts}, DeleteCases[Options[PowerGridFixedPoint],Method->Automatic]]]/.{angle\[Theta]->inputAngle,angularVelocity\[Omega]->inputAngularVelocity\[Omega]};
fixedPointAngles=Table[inputAngle[i],{i,1,numberOfNodes}]/.fixed;
(*The system can be started from the fixed point, NOTE: I do not pass the Method because I assume the Method is used for NDSolve*)
If[initialConditionInput=="FixedPoint",
initialConditionInput=Flatten[Table[{angle\[Theta][i][ t/;t<= 0]==inputAngle[i],angularVelocity\[Omega][i][ t/;t<= 0]==inputAngularVelocity\[Omega][i]},{i,1,numberOfNodes}]]/.fixed;];
(*Add any (initial) perturbance*)
initialConditionInput[[2,2]]+=frequencyOffsetInput;

(*attatch initial power entries, if they were not given already*)
If[Length[initialConditionInput]!= 3*numberOfNodes,
Flatten[AppendTo[initialConditionInput,Table[powerEntries[i][t/;t<=0]==powerVectorInput[[i]],{i,1,numberOfNodes}]]]];
(*Set power dynamics to be equal to zero, if no valid input is given*)
powerDynamics=Which[ListQ[powerDynamicsInput],powerDynamicsInput,
True,Table[Derivative[1][powerEntries[i]][t]==perturbationsInput[[i]],{i,1,numberOfNodes}]];
(*add perturbations to the power dynamics*)
Do[powerDynamics[[nodeIndex,2]]+=perturbationsInput[[nodeIndex]],{nodeIndex,1,numberOfNodes}];

With[{(*define and solve the differential equation*)
equations=Table[angle\[Theta][i]'[t]==angularVelocity\[Omega][i][t],{i,1,numberOfNodes}]\[Union]Table[inertiaVectorInput[[i]]*angularVelocity\[Omega][i]'[t]==powerEntries[i][t]-damping\[Alpha]Input[[i]]*angularVelocity\[Omega][i][t]-secondaryControlInput[[i]]*angle\[Theta][i][t]+Sum[dynamicCouplingMatrix[i,edgeNeighbors][t]*Sin[angle\[Theta][edgeNeighbors][t]-angle\[Theta][i][t]+phaseShiftMatrixInput[[i,edgeNeighbors]]],{edgeNeighbors,DeleteCases[Flatten[Cases[listOfEdges,{x_,y_}/;x==i\[Or]y==i]],i]}],{i,1,numberOfNodes}]\[Union]powerDynamics\[Union]Flatten[Table[{dynamicCouplingMatrix[edge[[1]],edge[[2]]][0]==couplingMatrixInput[[edge[[1]],edge[[2]]]],
dynamicCouplingMatrix[edge[[2]],edge[[1]]][0]==couplingMatrixInput[[edge[[1]],edge[[2]]]],
transientOverloadViolationTime[Position[listOfEdges,edge][[1,1]]][0]==10*tmaxInput},{edge,listOfEdges}]],

linearEquations=Table[angle\[Theta][i]'[t]==angularVelocity\[Omega][i][t],{i,1,numberOfNodes}]\[Union]Table[inertiaVectorInput[[i]]*angularVelocity\[Omega][i]'[t]==-damping\[Alpha]Input[[i]]*angularVelocity\[Omega][i][t]-secondaryControlInput[[i]]*angle\[Theta][i][t]
-Sum[dynamicCouplingMatrix[i,edgeNeighbors][t]*Cos[fixedPointAngles[[edgeNeighbors]]-fixedPointAngles[[i]]+phaseShiftMatrixInput[[i,edgeNeighbors]]]*(angle\[Theta][i][t]-fixedPointAngles[[i]]),{edgeNeighbors,DeleteCases[Flatten[Cases[listOfEdges,{x_,y_}/;x==i\[Or]y==i]],i]}]
+Sum[dynamicCouplingMatrix[i,edgeNeighbors][t]*Cos[fixedPointAngles[[edgeNeighbors]]-fixedPointAngles[[i]]+phaseShiftMatrixInput[[i,edgeNeighbors]]]*(angle\[Theta][edgeNeighbors][t]-fixedPointAngles[[edgeNeighbors]]),{edgeNeighbors,DeleteCases[Flatten[Cases[listOfEdges,{x_,y_}/;x==i\[Or]y==i]],i]}]
,{i,1,numberOfNodes}]\[Union]powerDynamics\[Union]Flatten[Table[{dynamicCouplingMatrix[edge[[1]],edge[[2]]][0]==couplingMatrixInput[[edge[[1]],edge[[2]]]],
dynamicCouplingMatrix[edge[[2]],edge[[1]]][0]==couplingMatrixInput[[edge[[1]],edge[[2]]]],
transientOverloadViolationTime[Position[listOfEdges,edge][[1,1]]][0]==10*tmaxInput},{edge,listOfEdges}]]
},
dynamicEquations=If[useLinearizedSwingInput,linearEquations,equations];
With[{(*internally store the solution to evaluate angularVelocity\[Omega] at tmax*)
powerGridSolution=NDSolve[{dynamicEquations\[Union]initialConditionInput\[Union]eventsInput},variables,{t,tminInput,tmaxInput},FilterRules[{opts}, Options[NDSolve]],DiscreteVariables->Flatten[Table[{dynamicCouplingMatrix[edge[[1]],edge[[2]]],dynamicCouplingMatrix[edge[[2]],edge[[1]]],transientOverloadViolationTime[Position[listOfEdges,edge][[1,1]]]},{edge,listOfEdges}]]]
},
(*Export parameters and results*)
If[exportResultsInput,SetDirectory[outputDirectoryInput];
Which[
(*export as h5 format, like Potsdam people do*)
exportFormatInput=="h5",
Export[outputNameInput<>".h5",{couplingMatrixInput,powerVectorInput,Map[ToString[#,InputForm,CharacterEncoding->"ASCII"]&,initialConditionInput],If[exportPertubationsInput,Map[ToString[#,InputForm,CharacterEncoding->"ASCII"]&,perturbationsInput],Table["output prevented",{numberOfNodes}]],damping\[Alpha]Input,priceElasticity\[Gamma]Input,delay\[Tau]Input,averagingTimeInput,{tmaxInput},(Table[angularVelocity\[Omega][i][t],{i,1,numberOfNodes}]/.powerGridSolution[[1]])/.{t->tmaxInput}},{"Datasets",{"couplingMatrix","powerVector","initialCondition","perturbations","damping\[Alpha]","priceElasticity\[Gamma]","delay\[Tau]","averagingTime","tmax","angularVelocity\[Omega]Solution[tmax]"}},CharacterEncoding->"ASCII"],
(*export Mathematica specific format*)
exportFormatInput=="WDX",
Export[outputNameInput<>".wdx",{"couplingMatrix"-> couplingMatrixInput,"powerVector"->powerVectorInput,
"damping\[Alpha]"->damping\[Alpha]Input,"priceElasticity\[Gamma]"->priceElasticity\[Gamma]Input,
"delay\[Tau]"->delay\[Tau]Input,"averagingTime"->averagingTimeInput,"tmax"->tmaxInput,
"initialCondition"->initialConditionInput,
(*Perturbations and trajectory will only be exported only if their special options are set to True*)
"perturbations"->If[exportPertubationsInput,Map[ToString[#,InputForm,CharacterEncoding->"ASCII"]&,perturbationsInput],"output prevented to save memory, set 'exportPertubations'->True to export Perturbations"]
,"trajectory"->If[exportTrajectoryInput,powerGridSolution,
"output prevented to save memory, set 'exportTrajectory'->True to export Trajectory"]}
,CharacterEncoding->"ASCII"],
True, Print["exportFormat has to be 'WDX' or 'h5'"]]
];
powerGridSolution
]
]
]


