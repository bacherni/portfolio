(*Power Grid Fixed Point*)
Options[PowerGridFixedPoint]=Union[Options[FindRoot] ,Options[PowerGridInput]];
PowerGridFixedPoint[opts:OptionsPattern[]]:=
With[(*use local version of functions*)
{},
PowerGridInput[Evaluate[FilterRules[{opts}, Options[PowerGridInput]]]];
(*Numerically find one fixed point of the power grid, the equation was simplified, using angularVelocity\[Omega][t]=0*)
fixedPoint=If[Total[secondaryControlInput]==0,
Join[FindRoot[Evaluate[Table[powerVectorInput[[i]]+Sum[couplingMatrixInput[[i,j]]*Sin[angle\[Theta][j]-angle\[Theta][i]+phaseShiftMatrixInput[[i,j]]],{j,1,numberOfNodes}]-secondaryControlInput[[i]]*angle\[Theta][i]==0,{i,2,numberOfNodes}]\[Union]{angle\[Theta][1]==0}],Table[{angle\[Theta][i],startingPointInput[[i]]},{i,1,numberOfNodes}]
,Evaluate[FilterRules[{opts}, Options[FindRoot]]](*we need Evaluate[] since FindRoot[] has Attrbiute HoldAll*)
],Table[angularVelocity\[Omega][i]->0,{i,1,numberOfNodes}]]
,
Join[FindRoot[Evaluate[Table[powerVectorInput[[i]]+Sum[couplingMatrixInput[[i,j]]*Sin[angle\[Theta][j]-angle\[Theta][i]+phaseShiftMatrixInput[[i,j]]],{j,1,numberOfNodes}]-secondaryControlInput[[i]]*angle\[Theta][i]==0,{i,1,numberOfNodes}]],Table[{angle\[Theta][i],startingPointInput[[i]]},{i,1,numberOfNodes}]
,Evaluate[FilterRules[{opts}, Options[FindRoot]]](*we need Evaluate[] since FindRoot[] has Attrbiute HoldAll*)
],Table[angularVelocity\[Omega][i]->0,{i,1,numberOfNodes}]]];
validated=DeleteDuplicates@Table[Evaluate[Abs[powerVectorInput[[i]]+Sum[couplingMatrixInput[[i,j]]*Sin[angle\[Theta][j]-angle\[Theta][i]+phaseShiftMatrixInput[[i,j]]],{j,1,numberOfNodes}]-secondaryControlInput[[i]]*angle\[Theta][i]]/.fixedPoint]<10^-10,{i,1,numberOfNodes}];
If[validated==={True},
Return[fixedPoint],
Return["Fixed point could not be found"]]
]


