(*PowerGridCascadeSimulation*)
Options[PowerGridCascadeSimulation]=Options[PowerGridInput];
PowerGridCascadeSimulation[opts:OptionsPattern[]]:=
Block[{
(*If these variables are not localized, at least the lineCut variable does not work properly*)
(*lineRules,transientOverloadEvents,*)listOfEdges,trigger,fixed,initial,customPerturbations,linesCut,(*sol,*)solLineCut,plot,plotLineCut,edgesCut,graph,graphLineCut,
numberOfUnsynchronizedNodesCascade,numberOfUnsynchronizedNodesSingleFailure,numberOfLinesCut,edgeDistances,
measures,nodesNotSynchronizedCascade,nodesNotSynchronizedSingleFailure,positionsOfZeroFlow,redundantLines
},
PowerGridInput[Evaluate[FilterRules[{opts}, Options[PowerGridInput]]]];
graph=AdjacencyGraph[Map[Unitize,couplingMatrixInput],VertexLabels->"Name"];
listOfEdges=Sort[Apply[List,EdgeList[graph],2]];
(*The system starts at the fixed point*)
fixed=PowerGridFixedPoint[Evaluate[FilterRules[{opts}, Options[PowerGridFixedPoint]]]]/.{angle\[Theta]->inputAngle,angularVelocity\[Omega]->inputAngularVelocity\[Omega]};
initial=Flatten[Table[{angle\[Theta][i][ t/;t<= 0]==inputAngle[i],angularVelocity\[Omega][i][ t/;t<= 0]==inputAngularVelocity\[Omega][i],powerEntries[i][t/;t<=0]==powerVectorInput[[i]]+powerOffsetInput},{i,1,numberOfNodes}]]/.fixed;
(*determine the initial flow*)
initialFlows=Abs[Table[couplingMatrixInput[[edge[[1]],edge[[2]]]]*Sin[inputAngle[edge[[1]]]-inputAngle[edge[[2]]]+phaseShiftMatrixInput[[edge[[2]],edge[[1]]]]]/.fixed,{edge,listOfEdges}]];
(*Remove all links that do not carry any current*)
positionsOfZeroFlow=Position[initialFlows,x_/;x<10^-8];
redundantLines=listOfEdges[[Flatten[positionsOfZeroFlow]]];
(*If the trigger line is carrying no current, do not simulate*)
If[MemberQ[redundantLines,triggerLineInput],Return[{Nothing}]];

(*store the initial flow for each non-0-flow line at time=0 to compare with maximal capacity later on. Note that I delete the trigger line and any entries with very small flow.*)
initialFlows=Delete[initialFlows,Position[listOfEdges,triggerLineInput]];
(*When using the initial flow as threshold get rid of all 0-flow lines in the graph as they have effectively 0-capacity*)
If[useInitialFlowThresholdInput,
reducedListOfEdges=Delete[listOfEdges,positionsOfZeroFlow];
graph=EdgeDelete[graph,Apply[UndirectedEdge,redundantLines,2]];
initialFlows=DeleteCases[initialFlows,x_/;x<10^-8];,
reducedListOfEdges=listOfEdges;
];
reducedListOfEdgesWithoutTrigger=DeleteCases[reducedListOfEdges,triggerLineInput];
flowOfEdge[edge_]:=initialFlows[[Position[reducedListOfEdgesWithoutTrigger,edge][[1,1]]]];
(*note that reducedListOfEdges, reducedListOfEdgesWithoutTrigger, initialFlows and flowOfEdge are all to be used by the PowerGridSolver and hence should not be localized*)


(*Unprotect when Event, then the list is build correctly*)
Unprotect[WhenEvent];Attributes[WhenEvent]=DeleteCases[Attributes[WhenEvent],HoldAll];
Off[AppendTo::rvalue];

(*Generate lists of events to cut additional lines: If useInitialFlowThresholdInput is set True, the tolerance \[Alpha]\[GreaterEqual]1 is compared to the initial flow on a line. Otherwise we cut lines when a certain tolerance 0\[LessEqual]\[Alpha]\[LessEqual]1 of their capacity K is reached. To do so, we check whether minimumTransientOverloadTimeInput\[Equal]0, then the lines are cut immeadieately. Otherwise we cut the line only, if we are again (hopefully still) above the threshold*)
If[useInitialFlowThresholdInput,
(*Use initial flow as threshold*)lineRules=Flatten[Table[WhenEvent[couplingMatrixInput[[edge[[1]],edge[[2]]]]*Abs[Sin[angle\[Theta][edge[[1]]][t]-angle\[Theta][edge[[2]]][t]+phaseShiftMatrixInput[[edge[[2]],edge[[1]]]]]]>=tolerance\[Alpha]Input*flowOfEdge[edge],{dynamicCouplingMatrix[edge[[1]],edge[[2]]][t]->0,dynamicCouplingMatrix[edge[[2]],edge[[1]]][t]->0
,AppendTo[linesCut,{{edge[[1]],edge[[2]]},t}],"RemoveEvent"}],{edge,reducedListOfEdgesWithoutTrigger}]];
transientOverloadEvents={};
initialOverloadEvent={};,

verySmallNumber=10^-10;
(*lines are shut down when the flow exceeds a constant times the maximum line capacity K*)
(*If the transient time \[Equal]0, the transientOverloadEvents won't work and I have to remove the links now*)
If[minimumTransientOverloadTimeInput==0,
lineRules=Flatten[
Table[WhenEvent[
(*NOTE: I had to add a small number to the right hand side because otherwise the WhenEvent algorithm gets stiffness problems*)
Abs[Sin[angle\[Theta][edge[[1]]][t]-angle\[Theta][edge[[2]]][t]+phaseShiftMatrixInput[[edge[[2]],edge[[1]]]]]]*dynamicCouplingMatrix[edge[[1]],edge[[2]]][t]>=tolerance\[Alpha]Input*couplingMatrixInput[[edge[[1]],edge[[2]]]]+verySmallNumber,
{dynamicCouplingMatrix[edge[[1]],edge[[2]]][t]->0,dynamicCouplingMatrix[edge[[2]],edge[[1]]][t]->0
,AppendTo[linesCut,{Evaluate[{edge[[1]],edge[[2]]}],t}],"RemoveEvent"
}],{edge,reducedListOfEdgesWithoutTrigger}]];
transientOverloadEvents={};
(*At t=triggerTime check which lines are already overloaded and cut those*)
initialOverloadEvent=Flatten[
Table[WhenEvent[t>=triggerTimeInput\[And]flowOfEdge[edge]>=tolerance\[Alpha]Input*couplingMatrixInput[[edge[[1]],edge[[2]]]],{dynamicCouplingMatrix[edge[[1]],edge[[2]]][t]->0,dynamicCouplingMatrix[edge[[2]],edge[[1]]][t]->0
,AppendTo[linesCut,{Evaluate[{edge[[1]],edge[[2]]}],t}]}]
,{edge,reducedListOfEdgesWithoutTrigger}]];,

(*lines are shut down when the flow exceeds a constant times the maximum line capacity K*)
(*When the line is overloaded for the first time, the time instance is saved so that we can check in te future whether the line load violation is over*)
lineRules=Flatten[
Table[WhenEvent[
(*Check whether the line is overloaded*)
Abs[Sin[angle\[Theta][edge[[1]]][t]-angle\[Theta][edge[[2]]][t]+phaseShiftMatrixInput[[edge[[2]],edge[[1]]]]]]*dynamicCouplingMatrix[edge[[1]],edge[[2]]][t]==tolerance\[Alpha]Input*couplingMatrixInput[[edge[[1]],edge[[2]]]]+verySmallNumber,
(*If that is true, save the initial trigger time which will be tested in transientOverloadEvents*)
{If[transientOverloadViolationTime[Position[listOfEdges,edge][[1,1]]][t]<10*tmaxInput,
Evaluate[transientOverloadViolationTime[Position[listOfEdges,edge][[1,1]]][t]->10*tmaxInput],
Evaluate[transientOverloadViolationTime[Position[listOfEdges,edge][[1,1]]][t]->t]
]}],{edge,reducedListOfEdgesWithoutTrigger}]];
(*When the line ist still overloaded and the minimum time has passed, cut the link*)
transientOverloadEvents=Flatten[
Table[WhenEvent[
t-transientOverloadViolationTime[Position[listOfEdges,edge][[1,1]]][t]>= minimumTransientOverloadTimeInput,
{dynamicCouplingMatrix[edge[[1]],edge[[2]]][t]->0,dynamicCouplingMatrix[edge[[2]],edge[[1]]][t]->0
,AppendTo[linesCut,{Evaluate[{edge[[1]],edge[[2]]}],t}],"RemoveEvent"}],{edge,reducedListOfEdgesWithoutTrigger}]];
(*At t=triggerTime check which lines are already overloaded and add them to the transientOverloadEvent check list*)
initialOverloadEvent=Flatten[
Table[WhenEvent[t>=triggerTimeInput\[And]flowOfEdge[edge]>=tolerance\[Alpha]Input*couplingMatrixInput[[edge[[1]],edge[[2]]]],transientOverloadViolationTime[Position[listOfEdges,edge][[1,1]]][t]->t]
,{edge,reducedListOfEdgesWithoutTrigger}]
];
]
];
Attributes[WhenEvent]={HoldAll};Protect[WhenEvent];
(*define a trigger event which cuts one line*)
trigger={WhenEvent[t==triggerTimeInput,{dynamicCouplingMatrix[triggerLineInput[[1]],triggerLineInput[[2]]][t]->0,dynamicCouplingMatrix[triggerLineInput[[2]],triggerLineInput[[1]]][t]->0,linesCut=Append[linesCut,{triggerLineInput,t}]}]};
(*-----------------------------------------------------------------------------------------------------------*)
Off[NDSolve::precw];
(*Initially, no lines were cut*)
linesCut={};
(*Add any (initial) perturbance*)
initial[[2,2]]=frequencyOffsetInput;
(*Variable to save that the solver was called by the cascading algorithm\[Rule] will only include non-0-flow edges*)
powerGridSolverCalledByCascades=True;
sol=If[useCascadingRulesInput,
(*Cascades by cutting lines*)
PowerGridSolverAdaptivePower[MaxSteps->\[Infinity],initialCondition->"FixedPoint",events->lineRules\[Union]trigger\[Union]transientOverloadEvents\[Union]initialOverloadEvent,Evaluate[FilterRules[{opts}, Options[PowerGridSolverAdaptivePower]]]],
(*Only cut the initial line and observe the frequencies*)
PowerGridSolverAdaptivePower[MaxSteps->\[Infinity],initialCondition->"FixedPoint",events->trigger,Evaluate[FilterRules[{opts}, Options[PowerGridSolverAdaptivePower]]]]
];
edgesCut=Table[{UndirectedEdge[linesCut[[i,1]][[1]],linesCut[[i,1]][[2]]],linesCut[[i,2]]},{i,1,Length[linesCut]}];
powerGridSolverCalledByCascades=False;
(*Visualizations*)
If[generateVisualizationsInput,
plot=If[useCascadingRulesInput,
(*Plotting cutting lines*)
Plot[Table[angularVelocity\[Omega][i][t]/.sol,{i,1,numberOfNodes}],{t,0,tmaxInput},Frame->True,FrameLabel->{"time","frequency deviation","overloaded lines cut"},LabelStyle->Directive[Bold,14,FontFamily->"Arial"],PlotStyle->Thick,ImageSize->Medium,PlotRange->Full],
(*Plotting the uncontrolled case*)
Plot[Table[angularVelocity\[Omega][i][t]/.sol,{i,1,numberOfNodes}],{t,0,tmaxInput},Frame->True,FrameLabel->{"time","frequency deviation","No additional action"},LabelStyle->Directive[Bold,14,FontFamily->"Arial"],PlotStyle->Thick,ImageSize->Medium,PlotRange->Full]
];
(*plot graph with removed lines*)
graphLineCut=EdgeDelete[graph,edgesCut[[;;,1]]]];
(*Measures*)
If[calculateMeasuresInput,
(*extract the number of unsynchronized nodes*)
measures=If[useCascadingRulesInput,
nodesNotSynchronizedCascade=Table[If[Abs[((angularVelocity\[Omega][i][t]/.sol)/.{t->tmaxInput})[[1]]]>\[Omega]NodeNotSynchronizedInput,i,Nothing],{i,1,numberOfNodes}];
numberOfUnsynchronizedNodesCascade=Length[nodesNotSynchronizedCascade];
(*number of lines cut*)
numberOfLinesCut=Length[edgesCut];
(*propagation of the failure: record the time and distance of the line cut with respect to the trigger line*)
edgeDistances=Table[If[GraphDistance[graph,triggerLineInput[[1]],additionalCut[[1,1]]]==GraphDistance[graph,triggerLineInput[[2]],additionalCut[[1,2]]],{GraphDistance[graph,triggerLineInput[[1]],additionalCut[[1,1]]],additionalCut[[2]]},{GraphDistance[graph,triggerLineInput[[1]],additionalCut[[1,2]]],additionalCut[[2]]}],{additionalCut,edgesCut}];
{"number of nodes not synchronized after cascade"->numberOfUnsynchronizedNodesCascade,
"number of lines cut"->numberOfLinesCut,
"lines cut"->linesCut,
"distanceMatrix"->edgeDistances,(*"edges cut"\[Rule]edgesCut,*)"nodes not synchronized after cascade"->nodesNotSynchronizedCascade},
nodesNotSynchronizedSingleFailure=Table[If[Abs[((angularVelocity\[Omega][i][t]/.sol)/.{t->tmaxInput})[[1]]]>\[Omega]NodeNotSynchronizedInput,i,Nothing],{i,1,numberOfNodes}];
numberOfUnsynchronizedNodesSingleFailure=Length[nodesNotSynchronizedSingleFailure];
{"number of nodes not synchronized after a single line cut"->numberOfUnsynchronizedNodesSingleFailure,
"nodes not synchronized after a single line cut"->nodesNotSynchronizedSingleFailure}
];
AppendTo[measures,"redundant (0-flow) lines"-> redundantLines]
];
Which[generateVisualizationsInput,GraphicsColumn[{plot,graphLineCut}],
calculateMeasuresInput,measures,
True,Return[]]
]


