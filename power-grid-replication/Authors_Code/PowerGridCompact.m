Options[PowerGridSolver]=Union[Options[NDSolve] ,Options[PowerGridInput]];
PowerGridSolver[opts:OptionsPattern[]]:=
Module[(*use local version of the function for the price*){price},
PowerGridInput[Evaluate[FilterRules[{opts}, Options[PowerGridInput]]]];
With[{(*define scalar variables as homogeneous lists*)
variables=Table[angle\[Theta][index],{index,1,numberOfNodes}]\[Union]Table[angularVelocity\[Omega][index],{index,1,numberOfNodes}]},
(*define price-frequency-relation, use averaging, if averagingTime[[i]]>0*)
If[useInverterDynamicsInput==False,
price[t_]:=Which[ 
Total[averagingTimeInput]==0,Table[-If[adaptationProportionalToPowerInput,Abs[powerVectorInput[[i]]],1]*priceElasticity\[Gamma]Input[[i]]*angularVelocity\[Omega][i][t-delay\[Tau]Input[[i]]],{i,1,numberOfNodes}],
True,Table[
Which[
averagingTimeInput[[i]]==0,-If[adaptationProportionalToPowerInput,Abs[powerVectorInput[[i]]],1]*priceElasticity\[Gamma]Input[[i]]*angularVelocity\[Omega][i][t-delay\[Tau]Input[[i]]],
averagingTimeInput[[i]]>0,-If[adaptationProportionalToPowerInput,Abs[powerVectorInput[[i]]],1]*priceElasticity\[Gamma]Input[[i]]/averagingTimeInput[[i]]*(angle\[Theta][i][t-delay\[Tau]Input[[i]]]-angle\[Theta][i][t-delay\[Tau]Input[[i]]-averagingTimeInput[[i]]]+averagingTimeInput[[i]]*measurementBiasInput[[i]]),
True,Print["Averaging time averagingTime has to be positive or zero."]; Return[]
],{i,1,numberOfNodes}]
];,
(*Note that the adaptation is proportional to the absolute value of the power input/output at the node*)
price[t_]:=Table[Piecewise[{
{-If[adaptationProportionalToPowerInput,Abs[powerVectorInput[[i]]],1]*adaptationRateInput*(angularVelocity\[Omega][i][t-delay\[Tau]Input[[i]]]-startConstantIntervalInput),angularVelocity\[Omega][i][t-delay\[Tau]Input[[i]]]<startConstantIntervalInput},
{0,startConstantIntervalInput<= angularVelocity\[Omega][i][t-delay\[Tau]Input[[i]]]<= endConstantIntervalInput},
{-If[adaptationProportionalToPowerInput,Abs[powerVectorInput[[i]]],1]*adaptationRateInput*(angularVelocity\[Omega][i][t-delay\[Tau]Input[[i]]]-endConstantIntervalInput),angularVelocity\[Omega][i][t-delay\[Tau]Input[[i]]]>endConstantIntervalInput}}],{i,1,numberOfNodes}];
];
With[{(*define and solve the differential equation*)
equations=Table[angle\[Theta][i]'[t]==angularVelocity\[Omega][i][t],{i,1,numberOfNodes}]\[Union]Table[inertiaVectorInput[[i]]*angularVelocity\[Omega][i]'[t]==powerVectorInput[[i]]+perturbationsInput[[i]]+price[t][[i]]-damping\[Alpha]Input[[i]]*angularVelocity\[Omega][i][t]-secondaryControlInput[[i]]*angle\[Theta][i][t]+Sum[couplingMatrixInput[[i,j]]*Sin[angle\[Theta][j][t]-angle\[Theta][i][t]+phaseShiftMatrixInput[[i,j]]],{j,1,numberOfNodes}],{i,1,numberOfNodes}]},
(*The system can be started from the fixed point, NOTE: I do not pass the Method because I assume the Method is used for NDSolve*)
If[initialConditionInput=="FixedPoint",
fixed=PowerGridFixedPoint[FilterRules[{opts}, DeleteCases[Options[PowerGridFixedPoint],Method->Automatic]]]/.{angle\[Theta]->inputAngle,angularVelocity\[Omega]->inputAngularVelocity\[Omega]};
initialConditionInput=Flatten[Table[{angle\[Theta][i][ t/;t<= 0]==inputAngle[i],angularVelocity\[Omega][i][ t/;t<= 0]==inputAngularVelocity\[Omega][i]},{i,1,numberOfNodes}]]/.fixed;];
(*Add any (initial) perturbance*)
initialConditionInput[[2,2]]+=frequencyOffsetInput;
With[{(*internally store the solution to evaluate angularVelocity\[Omega] at tmax*)
powerGridSolution=NDSolve[{equations\[Union]initialConditionInput},variables,{t,tminInput,tmaxInput},FilterRules[{opts}, Options[NDSolve]]]},
(*Export parameters and results*)
If[exportResultsInput,SetDirectory[outputDirectoryInput];
Which[
(*export as h5 format, like Potsdam people do*)
exportFormatInput=="h5",
Export[outputNameInput<>".h5",{couplingMatrixInput,powerVectorInput,Map[ToString[#,InputForm,CharacterEncoding->"ASCII"]&,initialConditionInput],If[exportPertubationsInput,Map[ToString[#,InputForm,CharacterEncoding->"ASCII"]&,perturbationsInput],Table["output prevented",{numberOfNodes}]],damping\[Alpha]Input,priceElasticity\[Gamma]Input,delay\[Tau]Input,averagingTimeInput,{tmaxInput},(Table[angularVelocity\[Omega][i][t],{i,1,numberOfNodes}]/.powerGridSolution[[1]])/.{t->tmaxInput}},{"Datasets",{"couplingMatrix","powerVector","initialCondition","perturbations","damping\[Alpha]","priceElasticity\[Gamma]","delay\[Tau]","averagingTime","tmax","angularVelocity\[Omega]Solution[tmax]"}},CharacterEncoding->"ASCII"],
(*export Mathematica specific format*)
exportFormatInput=="WDX",
Export[outputNameInput<>".wdx",{"couplingMatrix"-> couplingMatrixInput,"powerVector"->powerVectorInput,
